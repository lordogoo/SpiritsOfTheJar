public class tutorialendgroupspeach extends animation
{
    
    speechdisplay displaycomponent;
    
    public tutorialendgroupspeach(gamepanel gp,triggeranimations t,int durration,Color b,speechdisplay sd)
    {
        super(gp,t,durration,b,0);
        //assign animation data    
        displaycomponent = sd;
        
        animation = new Timeline(this);
        animation.addPropertyToInterpolate("currentframe",durration,0);
        animation.setDuration(750);
        animation.play();
    }

    public void update(){
        System.out.println("speech "+currentframe);
        displaycomponent.setcurrentframe(currentframe);
        displaycomponent.setlocation();
        displaycomponent.repaint();
        
        if((currentframe == 0)&&(active)){
            System.out.println("terminate animation");
            System.out.println(trig);
            active = false;
            trig.endanimation(target);
        }
    }
}
